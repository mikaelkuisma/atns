temp1 = Alg2_B_i;
temp0 = temp0 * temp1;
dAlg2_B_idt = dAlg2_B_idt + temp0;
dBflatdt = [ dAlg1_B_idt dAlg2_B_idt ];
Bflat2 = Bflat;
Bflat = Bflat + dBflatdt*dt;
Alg1_B_i = Bflat(1);
Alg2_B_i = Bflat(2);
