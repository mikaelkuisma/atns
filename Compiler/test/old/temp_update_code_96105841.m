temp1 = Alg2_B_i;
temp0 = temp0 * temp1;
dAlg2_B_idt = dAlg2_B_idt + temp0;
dBflat2dt = [ dAlg1_B_idt dAlg2_B_idt ];
Bflat = Bflat2 + (dBflatdt+dBflat2dt)*dt/2;
Alg1_B_i = Bflat(1);
Alg2_B_i = Bflat(2);
      end
   end
Bflat = [ Alg1_B_i Alg2_B_i ];
dAlg1_B_idt = 0;
dAlg2_B_idt = 0;
temp0 = Guild_C;
temp1 = Guild_s;
