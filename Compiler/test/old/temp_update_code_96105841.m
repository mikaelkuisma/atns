temp1 = Alg2_B_i;
temp0 = temp0 * temp1;
dAlg2_B_idt = dAlg2_B_idt + temp0;
dAlg1_B_idt = 0;
dAlg2_B_idt = 0;
dBflatdt = [ dAlg1_B_idt dAlg2_B_idt ];
Bflat = Bflat + dBflatdt;
Alg1_B_i = Bflat(1);
Alg2_B_i = Bflat(2);
